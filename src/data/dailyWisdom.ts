const VERSE_REFLECTION_WISDOMS: Wisdom[] = DAILY_VERSES.map((verse) => ({
  text: verse.arabic,
  source: `سورة ${verse.surahName} — الآية ${verse.ayahIndex}`,
  notes: verse.meaning,
}));

function normalizeWisdomText(value: string) {
  return value
    .replace(/[\u064B-\u065F\u0670\u0640]/g, "")
    .replace(/[ٱأإآ]/g, "ا")
    .replace(/[ىي]/g, "ي")
    .replace(/ة/g, "ه")
    .replace(/[^\u0621-\u064A0-9]+/g, "")
    .trim();
}

function uniqueWisdoms(items: Wisdom[]): Wisdom[] {
  const seen = new Set<string>();
  return items.filter((item) => {
    const key = normalizeWisdomText(item.text);
    if (!key || seen.has(key)) return false;
    seen.add(key);
    return true;
  });
}

export const DAILY_WISDOMS: Wisdom[] = uniqueWisdoms([
  ...BASE_DAILY_WISDOMS,
  ...VERSE_REFLECTION_WISDOMS,
]);

function hashDateKey(dateKey: string) {
  let hash = 0;
  for (let index = 0; index < dateKey.length; index += 1) {
    hash = (hash * 31 + dateKey.charCodeAt(index)) >>> 0;
  }
  return hash;
}

export function getTodayWisdom(dateKey?: string): Wisdom {
  const dayIndex = dateKey ? hashDateKey(dateKey) : Math.floor(Date.now() / 86400000);
  return DAILY_WISDOMS[dayIndex % DAILY_WISDOMS.length] as Wisdom;
}
